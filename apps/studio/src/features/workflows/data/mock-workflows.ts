import {
  type WorkflowNode,
  type WorkflowSystem,
  WorkflowSystemSchema,
} from "@/features/workflows/schema";

function canvasNode(
  id: string,
  label: string,
  type: string,
  metrics: WorkflowNode["metrics"],
  options: Pick<WorkflowNode, "state" | "provider" | "model"> = {},
): WorkflowNode {
  return {
    id,
    label,
    type,
    provider: options.provider ?? "Google",
    model: options.model ?? "gemini-2.5-flash",
    state: options.state ?? "healthy",
    metrics,
  };
}

